// Fonction pour obtenir les projets selon la langue
export const getProjects = (language = 'fr') => {
  // Mappage des codes de langue aux nouveaux codes pays
  const languageMap = {
    'zh': 'CN',  // Chinois
    'ja': 'JP',  // Japonais
    'ko': 'KR',  // Coréen
    'th': 'TH',  // Thaï
    'vi': 'VN'   // Vietnamien
  };
  
  // Convertir le code de langue si nécessaire
  const langCode = languageMap[language] || language;
  
  return projectsData[langCode] || projectsData.fr;
};

// Fonction pour obtenir les projets vedettes selon la langue
export const getFeaturedProjects = (language = 'fr') => {
  const projects = getProjects(language);
  return projects.filter(project => project.featured);
};

